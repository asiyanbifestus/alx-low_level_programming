#include "3-calc.h"

/**
 * op_add - calculates the sum of two integers
 * @a: the first integer
 * @b: the second integer
 * Return: sum of a and b
 */

int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - subtracts two integers
 * @a: the first integer
 * @b: the second integer
 * Return: difference o the two integers
 */

int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - multiplies two inter
 * @a: the first integer
 * @b: the second inter
 * Return: the product value of a and b
 */

int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - divides two integers
 * @a: the first integer
 * @b: the second integer
 * Return; the result of division of a and b
 */

int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - calculates the reminder of the division of the two integers
 * mod stands for modulus in mathematics
 * @a: the first integer
 * @b: the second integer
 * Return: remainder of  a and b
 */

int op_mod(int a, int b)
{
return (a % b);
}
